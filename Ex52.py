print("Program to check your grade based on Grade point average")

grade = float(input("Enter your grade point average"))

if(grade == 5.0):
    print("Your grade is A+")
elif(grade == 4.0):
    print("Your grade is A")
elif(grade == 3.7):
    print("Your grade is A-")
elif(grade == 3.3):
    print("Your grade is B+")
elif(grade == 3.0):
    print("Your grade is B")
elif(grade == 2.7):
    print("Your grade is a B-")
elif(grade == 2.3):
    print("Your grade is a C+")
elif(grade == 2.0):
    print("Your grade is a C")
elif(grade == 1.7):
    print("Your grade is a C-")
elif(grade == 1.3):
    print("Your grade is a D+")
elif(grade == 1.0):
    print("Your grade is a D")
elif(grade == 0):
    print("Your grade is an F")